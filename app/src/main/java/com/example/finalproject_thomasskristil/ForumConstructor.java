package com.example.finalproject_thomasskristil;


public class ForumConstructor {

    private String name;
    private String photoUrl;
    private String text;

    public ForumConstructor() { //empty constructor needed
    }

    public ForumConstructor(String text, String name, String photoUrl) {
        this.text = text;
        this.name = name;
        this.photoUrl = photoUrl;
    }

    public String getText() {
        return text;
    }

    public void setText(String text) {
        this.text = text;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPhotoUrl() {
        return photoUrl;
    }

    public void setPhotoUrl(String photoUrl) {
        this.photoUrl = photoUrl;
    }
}


